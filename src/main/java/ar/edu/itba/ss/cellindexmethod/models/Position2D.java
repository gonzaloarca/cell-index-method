package ar.edu.itba.ss.cellindexmethod.models;

public class Position2D {
    private double x;
    private double y;

    public Position2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
